# funzioni per l'analisi esplorativa

dim() # ci restituisce il numero dei casi e il numero delle variabili di un dataset

str() # il tipo di dati, il numero di casi e di variabili e il tipo di ognuna

summary() # un riepilogo di alcuni elementi, come valori minimi, massimi, media, mediana e quantili di ogni variabile

nrow() # il numero di righe

ncol() # il numero di colonne

names() # i nomi delle variabili

class() # il tipo di oggetto

head() # i primi casi di un dataset

tail() # gli ultimi casi di un dataset

mean() # la media

median() # la mediana 

quantile() # i quantili
